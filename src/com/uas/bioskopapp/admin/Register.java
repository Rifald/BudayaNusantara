    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        nama=String.valueOf(namaTf.getText());
        alamat=String.valueOf(alamatTf.getText());
        username=String.valueOf(userTf.getText());
        password=String.valueOf(passTf.getText());
        hp=String.valueOf(hpTf.getText());
        

        try {
            sqlcheck = "select * from admin where username = '"+ username+ "'"; 
                                    kdb.bukaKoneksi();
            koneksi = kdb.getConn();
            cs = koneksi.createStatement();
            Rslogin = cs.executeQuery(sqlcheck);
            if(Rslogin.next()){
                if(userTf.getText().equals(Rslogin.getString("username"))){
                    JOptionPane.showMessageDialog(null, "Username sudah terdaftar, Silahkan mengganti username");
                }else{
                    JOptionPane.showMessageDialog(null, "sip");
                }
            }else if("".equals(namaTf.getText())){
                JOptionPane.showMessageDialog(null, "Harap Isi Nama");
            }else if("".equals(userTf.getText())){
                JOptionPane.showMessageDialog(null, "Harap Isi Username");
            }else if("".equals(passTf.getText())){
                JOptionPane.showMessageDialog(null, "Harap Isi Password");
            }else if("".equals(hpTf.getText())){
                JOptionPane.showMessageDialog(null, "Harap Isi No. Hp");
            }
            else if("".equals(alamatTf.getText())){
                JOptionPane.showMessageDialog(null, "Harap Isi Alamat");
            }else{
                    sql="INSERT INTO admin(nama,alamat, no_hp, username, password ) value"
                    + "('"+ nama  +"','"+ alamat +"','"+ hp +"','"+username +"','"
                    +  password  +"')";

                cs.execute(sql);

                JOptionPane.showMessageDialog(null,"Registrasi Berhasil");
                Login login = new Login();
                login.setVisible(true);
                 dispose();
                }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Registrasi Gagal \n"+e.getMessage());
        }
                               
    }//GEN-LAST:event_submitBtnActionPerformed

